	getText: function(key) {
		
		// Get current language
		const library = this.translations[window.craConfig.currentLanguage];
		
		// Check if the key exists
		if(!library.hasOwnProperty(key)) {
			return null;
		} 
		
		// Get the value of the key
		return library[key];		
	}, 
	
	// Language toggle
    toggleLanguage: function() {
        window.craConfig.currentLanguage = window.craConfig.currentLanguage === 'EN' ? 'FR' : 'EN';
        const langToggle = document.querySelector('.language-toggle');
        langToggle.textContent = window.craConfig.currentLanguage === 'EN' ? 'Français' : 'English';
        
        // Update UI text
        this.updateUILanguage();
        
        // Reload current category if any
        if (window.craConfig.currentCategory) {
            this.loadCategory(window.craConfig.currentCategory);
        }
    },

    updateUILanguage: function() {
        const t = this.translations[window.craConfig.currentLanguage];
        
        // Update all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (t[key]) {
                element.textContent = t[key];
            }
        });     
		
    },



